
// ------Basic Arithmetic Operator--------
// + Addition 
// - Subtraction 
// * Multiplication
// / Division 
// % Modulus
// ++ Increment 
// -- Decrement 
// ** Exponentiation 

// increment x++
let x = 5;
x++;
console.log(x);

// decrement n--
let n = 5;
n--;
console.log(n);

// % modulus *Remainder after division 
let a = 5 % 2;  // this operator is widely used in gaming logic and loop condition
console.log('output =', a);

//  *multiplication scale numbers /division splits numbers into
let b = 5 * 2;
let c = 5 /2;

console.log('output =', b, c);
console.log('output =', c);

// + addition - subtraction 
let d = 5 + 10;
let e = 15 - 5;

console.log('output =', d);
console.log('output =', e);